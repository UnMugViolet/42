void	ft_putchar(char c);

void	ft_print_params(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	(void)argc;
	while (argv[i])
	{
		ft_print_params(argv[i]);
		ft_putchar('\n');
		i++;
	}
}
